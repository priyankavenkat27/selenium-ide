//BITWISE OPERATORS

public class bitwiseopts {
    public static void main(String[] args) {
        int a = 5;  // 0101
        int b = 3;  // 0011

        System.out.println("a & b = " + (a & b)); // 0001 = 1  //  bit wise opts
        System.out.println("a | b = " + (a | b)); // 0111 = 7  //bit wise opts
        System.out.println("a ^ b = " + (a ^ b)); // 0110 = 6  //bit wise opts
    }
}

