public class switch18 {
    public static void main(String[] args) {
        
    
    char grade='A';
    switch(grade){
        case 'A': 
        System.out.println("EXCELLENT");
        break;

        case 'B': 
        System.out.println("Good");
        break;

        case 'C': 
        System.out.println("okay,can improve");
        break;

        case 'D': 
        System.out.println("Failed");
        break;
    }
}}
